 // two pointers
///////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////
#include <iostream>
using namespace std;

int main() {
	//input
	int T ,N ;
	cin>>T>>N;
	int A[N];
	for(int k =0 ; k<N ;k++){ cin>>A[k];}
	
	//main functions
    int j = N-1 , i =0 ,temp;
    for(i=0; i<j; i++,j--){
        temp = A[i];
        A[i]=A[j];
        A[j] = temp;
        // cout<<A[i]<<A[j-i]<<endl;
        
    }
    for(int i =0; i<N ; i++){ cout<<A[i]<<" ";}
    return 0;
}
